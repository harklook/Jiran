"""## 🔄 Step 6: Generate Transfer Recommendations"""

def generate_transfer_recommendations(stock_features):
    """Find transfer opportunities between stores"""

    recommendations = []

    print("🔍 Finding transfer opportunities...\n")

    for product_id in stock_features['product_id'].unique():
        product_stock = stock_features[stock_features['product_id'] == product_id]

        # Low stock stores
        low_stores = product_stock[product_stock['label'] == 'Low']

        # Excess stock stores
        excess_stores = product_stock[product_stock['label'].isin(['Excess', 'Slow', 'Dead'])]

        # Match
        for _, low_row in low_stores.iterrows():
            for _, excess_row in excess_stores.iterrows():
                if low_row['store_id'] != excess_row['store_id']:

                    deficit = max(0, low_row['forecast_30d'] - low_row['stock_qty'])
                    surplus = max(0, excess_row['stock_qty'] - excess_row['forecast_30d'])

                    transfer_qty = min(deficit, surplus)

                    if transfer_qty > 0:
                        recommendations.append({
                            'product_id': product_id,
                            'from_store': excess_row['store_id'],
                            'from_stock': int(excess_row['stock_qty']),
                            'from_label': excess_row['label'],
                            'to_store': low_row['store_id'],
                            'to_stock': int(low_row['stock_qty']),
                            'to_label': low_row['label'],
                            'transfer_qty': int(transfer_qty),
                            'priority': 'HIGH' if low_row['label'] == 'Low' else 'MEDIUM',
                        })

    return pd.DataFrame(recommendations).sort_values('transfer_qty', ascending=False)

