def prepare_prophet_data(transactions):
    """Aggregate to daily totals per (store, product)"""
    transactions['date'] = transactions['timestamp'].dt.date

    daily = transactions.groupby(['store_id', 'product_id', 'date'], as_index=False).agg({
        'quantity': 'sum'
    }).rename(columns={'date': 'ds', 'quantity': 'y'})

    daily['ds'] = pd.to_datetime(daily['ds'])
    return daily

