from prophet import Prophet
import time

def train_prophet_models(daily_data, top_n=100, horizons=[7, 14, 30, 60]):
    """
    Train Prophet models and generate AGGREGATED forecasts
    Returns one row per (store, product) with forecast_7d, forecast_14d, etc.
    """

    # Get top products by sales
    top_products = daily_data.groupby('product_id')['y'].sum().nlargest(top_n).index

    models = {}
    forecasts_list = []

    max_horizon = max(horizons)

    print(f"\n🚀 Training Prophet models for top {top_n} products...")
    print(f"   Forecast horizons: {horizons} days")
    print(f"   Estimated time: ~{top_n * 50 * 0.4 / 60:.0f} minutes\n")

    start_time = time.time()
    model_count = 0

    for idx, product_id in enumerate(top_products):
        print(f"[{idx+1}/{len(top_products)}] {product_id}...", end=" ")

        product_data = daily_data[daily_data['product_id'] == product_id]
        stores_processed = 0

        for store_id in product_data['store_id'].unique():
            store_product_data = product_data[product_data['store_id'] == store_id][['ds', 'y']]

            if len(store_product_data) < 14:
                continue

            try:
                model = Prophet(
                    daily_seasonality=False,
                    weekly_seasonality=True,
                    yearly_seasonality=False,
                    interval_width=0.95
                )
                model.fit(store_product_data)

                # Forecast max horizon
                future = model.make_future_dataframe(periods=max_horizon)
                forecast = model.predict(future)

                # Get future forecasts only
                forecast_future = forecast.tail(max_horizon).copy()

                # Aggregate for each horizon
                row = {
                    'store_id': store_id,
                    'product_id': product_id
                }

                for horizon in horizons:
                    horizon_data = forecast_future.head(horizon)
                    row[f'forecast_{horizon}d'] = round(horizon_data['yhat'].clip(lower=0).sum(), 1)
                    row[f'forecast_{horizon}d_lower'] = round(horizon_data['yhat_lower'].clip(lower=0).sum(), 1)
                    row[f'forecast_{horizon}d_upper'] = round(horizon_data['yhat_upper'].clip(lower=0).sum(), 1)

                forecasts_list.append(row)
                models[f"{store_id}_{product_id}"] = model
                stores_processed += 1
                model_count += 1

            except Exception as e:
                continue

        elapsed = time.time() - start_time
        avg_time = elapsed / (idx + 1)
        remaining = avg_time * (len(top_products) - idx - 1) / 60

        print(f"✓ ({stores_processed} stores, ~{remaining:.0f} min left)")

    forecasts_df = pd.DataFrame(forecasts_list)

    total_time = (time.time() - start_time) / 60
    print(f"\n✅ Training complete in {total_time:.1f} minutes")
    print(f"   Models trained: {model_count}")
    print(f"   Forecast records: {len(forecasts_df):,} rows (one per store-product)")

    # Validation check
    if model_count < 1000:
        print("\n⚠️  WARNING: Only trained", model_count, "models (expected ~5,000)")
        print("   This likely means you uploaded OLD data with fewer stores/products!")

    return models, forecasts_df

